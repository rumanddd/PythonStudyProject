# This will show you how to assign multiple values to variables
# This is called tuple unpacking

cat = ['Beige', 'Tall', 'Energetic']  # Define a list with three values
color, height, energy = cat  # Assign each value to a separate variable
print(color, height, energy)  # Print the unpacked values

